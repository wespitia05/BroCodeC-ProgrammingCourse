#include <iostream> // functions for basic input/output operations

// begin program by invoking the main function
int main() {
    // std = standard, cout = character output, << = output, endl = endline (flushes output buffer)
    std::cout << "hello world" << std::endl;
    std::cout << "i like pizza" << '\n'; // '\n' also works

    return 0; // exit the program, when reached no problems faced
}